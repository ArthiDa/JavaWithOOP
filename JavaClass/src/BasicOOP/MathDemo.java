
package BasicOOP;

/**
 *
 * @author ARTHI
 */
public class MathDemo {
    
    public static void main(String[] args) {
        
        System.out.println(Math.PI);
        System.out.println(Math.E);
        System.out.println(Math.exp(2));
        System.out.println(Math.abs(-5));
        System.out.println(Math.sqrt(169));
        System.out.println(Math.pow(2, 5));
        System.out.println(Math.max(4, 10));
        System.out.println(Math.min(4, 10));
        System.out.println(Math.floor(5.8));//Down
        System.out.println(Math.ceil(5.2));//Up
        System.out.println(Math.round(5.5));
        System.out.println(Math.log10(6));
        
        

    }
    
}
